def render_chat_interface():
    """渲染聊天界面"""
    st.title("🤖 AI 智能导游")
    st.markdown("我是您的专属导游助手\"小庐\"，有任何关于庐山的问题都可以问我！")

    # 初始化聊天历史
    if "chat_messages" not in st.session_state:
        st.session_state.chat_messages = []

    # 显示历史消息
    for message in st.session_state.chat_messages:
        with st.chat_message(message["role"]):
            st.markdown(message["content"])

    # 用户输入
    if question := st.chat_input("请输入您的问题..."):
        # 显示用户消息
        st.session_state.chat_messages.append({"role": "user", "content": question})
        with st.chat_message("user"):
            st.markdown(question)

        # 获取 AI 回答
        with st.chat_message("assistant"):
            with st.spinner("小庐正在思考..."):
                answer = get_ai_guide_response(question)
            st.markdown(answer)

        # 保存 AI 消息
        st.session_state.chat_messages.append({"role": "assistant", "content": answer})

    # 清空对话
    st.sidebar.button("🔄 清空对话", on_click=lambda: st.session_state.update(chat_messages=[]))

    # 常见问题
    with st.sidebar.expander("💡 常见问题"):
        st.markdown("""
        - 庐山有哪些必去景点？
        - 怎么去庐山？交通方便吗？
        - 门票多少钱？有优惠吗？
        - 庐山住宿推荐？
        - 什么时候去庐山最合适？
        - 庐山有什么历史文化？
        """)
